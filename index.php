function popup(nm, e) {
    popupElm = document.getElementById("popup-bg");
    if (nm == -1) {
        popupElm.classList.remove('visible');
        document.querySelector('body').style.overflow = 'auto';
    } else {
        elm = popupElm.getElementsByClassName('popup');
        if (typeof nm != 'undefined')
            for (var i = elm.length - 1; i >= 0; i--) {
                if (elm[i].id == 'popup' + nm) {

              		if ( typeof e != 'undefined' && nm == 4 ) {
              			$('#popup4 [name="comment"]').val( $(e).data('text') )
              			
              			if ( $(e).data('text') == 'Рассчитать предпроект Премиум' ) {
            					$('.popheader').html(`Рассчитать тариф “Премиум” <br class="pc"> и проконсультироваться с инженером`);
              			}
              			if ( $(e).data('text') == 'Рассчитать предпроект Стандарт' ) {
            					$('.popheader').html(`Рассчитать тариф “Стандарт” <br class="pc"> и проконсультироваться с инженером`);
              			}
              		}

                    elm[i].classList.add('visible');
                    popupElm.classList.add('visible');
                    document.querySelector('body').style.overflow = 'hidden';
                } else {
                    elm[i].classList.remove('visible');
                }
            }
        else {
            popupElm.classList.add('visible');
            document.querySelector('body').style.overflow = 'hidden';
        }
    }
}


